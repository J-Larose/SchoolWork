/**
 * Book class
 */
public class Book{
  private final String   name;
  private final String   author;
  
  /* getter methods */
  public String getName(){ return name; }
  public String getAuthor(){ return author; }
  
  /* constructor */
  public Book(String name, String author){
    this.name = name;
    this.author = author;
  }
  
}




